figure = input("Enter figure (circle, rectangle, triangle): ")

if figure == "circle":
    print("Calculating the area of circle")

    import math

    r = float(input("Enter the radius: "))
    area = math.pi * r ** 2
    print("area of circle:", area)

elif figure == "rectangle":
    print("Calculating the area of rectangle")

    a = float(input("Enter the length: "))
    b = float(input("Enter the width: "))
    area = a * b
    print("area of rectangle:", area)

elif figure == "triangle":
    print("Calculating the area of triangle")

    a = float(input("Enter the width: "))
    h = float(input("Enter the height: "))
    area = 0.5 * a * h
    print("area of triangle:", area)

else:
    print("unknown")


